#include<stdio.h>
int main()
{
	int  n,i,a,b,c,d,e,f,g;
	while(scanf("%d",&n)!=-1){
		if(n>=1000){
			a=n/1000;
		for(i=0;i<a;i++){
			printf("M");
		}
		n%=1000;
		}
		if(n>=100){
			a=n/100;
		if(a==4)
		printf("CD");
		else if(a==9)
		printf("CM");
		else{
			if(a<5){
				for(i=0;i<a;i++){
				printf("C");
			}
			}
			else{
				a=a-5;
				printf("D");
				for(i=0;i<a;i++){
				printf("C");
			}
			}
		}
		n%=100;
		}
		if(n>=10){
			a=n/10;
		if(a==4)
		printf("XL");
		else if(a==9)
		printf("XC");
		else{
			if(a<5){
				for(i=0;i<a;i++){
				printf("X");
			}
			}
			else{
				a=a-5;
				printf("L");
				for(i=0;i<a;i++){
				printf("X");
			}
			}
		}
		n%=10;
		}
		if(n>=1){
			a=n/1;
		if(a==4)
		printf("IV");
		else if(a==9)
		printf("IX");
		else{
			if(a<5){
				for(i=0;i<a;i++){
				printf("I");
			}
			}
			else{
				a=a-5;
				printf("V");
				for(i=0;i<a;i++){
				printf("I");
			}
			}
		}
		}
		printf("\n");
	}
	return 0; 
}

